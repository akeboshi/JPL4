package JPL.ch09.ex09_01;

class InfinityOperand{
	public static void main(String[] args) {
		System.out.println("Negative_infinity + Positive_infinity = " + (Double.NEGATIVE_INFINITY + Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity + Negative_infinity = " + (Double.NEGATIVE_INFINITY + Double.NEGATIVE_INFINITY));
		System.out.println("Positive_infinity + Positive_infinity = " + (Double.POSITIVE_INFINITY + Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity - Positive_infinity = " + (Double.NEGATIVE_INFINITY - Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity - Negative_infinity = " + (Double.NEGATIVE_INFINITY - Double.NEGATIVE_INFINITY));
		System.out.println("Positive_infinity - Positive_infinity = " + (Double.POSITIVE_INFINITY - Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity / Positive_infinity = " + (Double.NEGATIVE_INFINITY / Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity / Negative_infinity = " + (Double.NEGATIVE_INFINITY / Double.NEGATIVE_INFINITY));
		System.out.println("Positive_infinity / Positive_infinity = " + (Double.POSITIVE_INFINITY / Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity * Positive_infinity = " + (Double.NEGATIVE_INFINITY * Double.POSITIVE_INFINITY));
		System.out.println("Negative_infinity * Negative_infinity = " + (Double.NEGATIVE_INFINITY * Double.NEGATIVE_INFINITY));
		System.out.println("Positive_infinity * Positive_infinity = " + (Double.POSITIVE_INFINITY * Double.POSITIVE_INFINITY));
	}
}